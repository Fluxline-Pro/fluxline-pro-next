/**
 * Get featured testimonials
 */
export const getFeaturedTestimonials = (): Testimonial[] => {
  return testimonialsData.filter((t) => t.featured);
};

/**
 * Get all testimonials sorted by date (newest first)
 */
export const getAllTestimonials = (): Testimonial[] => {
  return [...testimonialsData].sort((a, b) => b.date.getTime() - a.date.getTime());
};

/**
 * Get testimonials by category
 */
export const getTestimonialsByCategory = (category: string): Testimonial[] => {
  return testimonialsData.filter((t) => t.category === category);
};

/**
 * Get testimonials by service
 */
export const getTestimonialsByService = (service: string): Testimonial[] => {
  return testimonialsData.filter((t) => t.services.includes(service));
};
